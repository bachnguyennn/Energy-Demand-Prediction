import pandas as pd
import numpy as np
import os
import holidays

PREPROCESSED_DATA_PATH = os.path.join(os.path.dirname(__file__), "..", "data", "processed", "AEP_hourly_preprocessed.csv")
FEATURES_DATA_PATH = os.path.join(os.path.dirname(__file__), "..", "data", "processed", "AEP_hourly_features.csv")

def add_time_features(df):
    df['hour'] = df['datetime'].dt.hour
    df['dayofweek'] = df['datetime'].dt.dayofweek
    df['month'] = df['datetime'].dt.month
    df['dayofyear'] = df['datetime'].dt.dayofyear
    df['year'] = df['datetime'].dt.year
    df['is_weekend'] = df['dayofweek'].isin([5, 6]).astype(int)
    
    # Cyclical encoding
    df['hour_sin'] = np.sin(2 * np.pi * df['hour']/23.0)
    df['hour_cos'] = np.cos(2 * np.pi * df['hour']/23.0)
    df['month_sin'] = np.sin(2 * np.pi * df['month']/12.0)
    df['month_cos'] = np.cos(2 * np.pi * df['month']/12.0)
    
    return df

def add_holiday_features(df):
    us_holidays = holidays.US()
    # Apply holiday check using the date part
    df['is_holiday'] = df['datetime'].dt.date.apply(lambda x: 1 if x in us_holidays else 0)
    return df

def add_lag_and_rolling_features(df):
    # Lags: 1 hour, 24 hours (1 day), 168 hours (7 days)
    df['lag_1h'] = df['load_mw'].shift(1)
    df['lag_24h'] = df['load_mw'].shift(24)
    df['lag_168h'] = df['load_mw'].shift(168)
    
    # Rolling stats on 24h and 168h windows
    # Note: Using shift(1) so we don't leak the current value into the rolling features
    df['rolling_mean_24h'] = df['load_mw'].shift(1).rolling(window=24).mean()
    df['rolling_std_24h'] = df['load_mw'].shift(1).rolling(window=24).std()
    
    df['rolling_mean_168h'] = df['load_mw'].shift(1).rolling(window=168).mean()
    df['rolling_std_168h'] = df['load_mw'].shift(1).rolling(window=168).std()
    
    # 30 day rolling mean (720 hours)
    df['rolling_mean_720h'] = df['load_mw'].shift(1).rolling(window=720).mean()

    return df

def main():
    print("Loading preprocessed data...")
    if not os.path.exists(PREPROCESSED_DATA_PATH):
        print(f"Error: {PREPROCESSED_DATA_PATH} not found. Run preprocess.py first.")
        return
        
    df = pd.read_csv(PREPROCESSED_DATA_PATH)
    df['datetime'] = pd.to_datetime(df['datetime'])
    
    print("Adding time and calendar features...")
    df = add_time_features(df)
    
    print("Adding holiday features...")
    df = add_holiday_features(df)
    
    print("Adding lag and rolling features...")
    df = add_lag_and_rolling_features(df)
    
    # Drop NaNs generated by shifting and rolling
    df = df.dropna().reset_index(drop=True)
    
    os.makedirs(os.path.dirname(FEATURES_DATA_PATH), exist_ok=True)
    df.to_csv(FEATURES_DATA_PATH, index=False)
    print(f"Feature engineering complete. Saved to {FEATURES_DATA_PATH}")
    print(f"Dataset shape after feature engineering: {df.shape}")

if __name__ == "__main__":
    main()
